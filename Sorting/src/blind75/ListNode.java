package blind75;

/**
 * Definition for singly-linked list node.
 * This is the standard ListNode structure used in LeetCode problems.
 */
class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

